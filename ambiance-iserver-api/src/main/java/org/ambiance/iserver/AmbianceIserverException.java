package org.ambiance.iserver;

public class AmbianceIserverException extends Exception {

	private static final long serialVersionUID = 4954998407375205958L;

	public AmbianceIserverException() {
		super();
	}

	public AmbianceIserverException(String arg0) {
		super(arg0);
	}

	public AmbianceIserverException(String arg0, Throwable arg1) {
		super(arg0, arg1);
	}

	public AmbianceIserverException(Throwable arg0) {
		super(arg0);
	}

}
