package org.ambiance.chain;

public class AmbianceChainException extends Exception {

	/**
	 * 
	 */
	private static final long serialVersionUID = -8321775654113814159L;

	public AmbianceChainException() {
		super();
	}

	public AmbianceChainException(String arg0) {
		super(arg0);
	}

	public AmbianceChainException(String arg0, Throwable arg1) {
		super(arg0, arg1);
	}

	public AmbianceChainException(Throwable arg0) {
		super(arg0);
	}

}
